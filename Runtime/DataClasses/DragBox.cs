namespace TUDarmstadt.SeriousGames.MoodleQuizParser
{
    public class DragBox
    {
        /// <value>Property <c>Text</c> represents the Text of this DragBox.</value>
        public string Text;
        /// <value>Property <c>Group</c> represents the Group of this DragBox.</value>
        public int Group;
        /// <value>Property <c>Index</c> represents the Index of the DragBox, used in the Text of the Drag And Drop on a text question to signal which answer needs to go where.</value>
        public int Index;
    }
}